package day20_ForLoop;

public class LoopWithoutCurlyBrace {
    public static void main(String[] args) {

        for(int i=1;i<=5;i++) {
            System.out.println("Hello");
            System.out.println("How are you");
        }


        System.out.println("============================");

        for(int i=1;i<=5;i++)
            System.out.println("Hello");//if there is only one statement
        System.out.println("How are you");//without curly brace
        //only the first statement print 5 times







    }
}
