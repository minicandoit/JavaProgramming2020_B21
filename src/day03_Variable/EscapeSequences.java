package day03_Variable;

public class EscapeSequences {

    public static void main(String[] args) {
// \n:
        System.out.println("Hello Everyone");
        System.out.println("My name is Mini Bae");


        System.out.println("=========================");

        System.out.println("Hello Everyone\nMy name is Mini Bae");

        System.out.println("=================");
        // \t:
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("===================");

        //  \\: \
        System.out.println("/");  //prints single frontslash
        System.out.println("\\"); // prints single backslash

        System.out.println("==========");

        System.out.println("My name is 'mini'");
        System.out.println("My name is \'MIni\'");

        System.out.println("==============");
// \":
        System.out.println("\"SUPER BOOK\"");
    }


}
