package day03_Variable;

public class CalculateCircle {

    public static void main(String[] args) {

        double Radius = 4.5;
        double PI = 3.14;

        double area = Radius * Radius;
        double Perimeter= 2*Radius*PI;

        System.out.println(area);
        System.out.println(Perimeter);
    }
}


