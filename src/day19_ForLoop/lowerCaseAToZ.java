package day19_ForLoop;

public class lowerCaseAToZ {
    public static void main(String[] args) {

        for(char i='a';i<='z';i++){
            System.out.print(i+" ");

        }

        System.out.println();
        System.out.println();
        for(char i='A';i<='Z';i++){
            System.out.print(i+" ");
        }

        for(char i=1;i<=30000;i++){
            System.out.print(i+" ");
        }






    }
}
