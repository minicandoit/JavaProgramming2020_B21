public class EscapeSequence_Recap {
    public static void main(String[] args) {


        System.out.println("\t Hello Everyone\n\tHello Batch21\nToday I learned new 'programming' \"Java\"");

//\' \' single quote you can use it but no need

        System.out.println("My favorite quote is: \n\t\"Winter is Coming\"\n\t\t\t\t-Ned Stark");
        System.out.println("====================================");

        System.out.println("\\");  //for single \

        System.out.println("Hi Cybertek\n\tToday is a great day!\nI am happy to be part of \"Batch 21\".");
    }

}
